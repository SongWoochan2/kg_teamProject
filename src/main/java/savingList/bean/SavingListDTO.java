package savingList.bean;

public class SavingListDTO {
	private int saving_code;
	private String saving_id;
	private String saving_name;
	private int pay_cost;
	private int saving_cost;
	private String saving_date;
	
	public int getSaving_code() {
		return saving_code;
	}
	public void setSaving_code(int saving_code) {
		this.saving_code = saving_code;
	}
	public String getSaving_id() {
		return saving_id;
	}
	public void setSaving_id(String saving_id) {
		this.saving_id = saving_id;
	}
	public String getSaving_name() {
		return saving_name;
	}
	public void setSaving_name(String saving_name) {
		this.saving_name = saving_name;
	}
	public int getPay_cost() {
		return pay_cost;
	}
	public void setPay_cost(int pay_cost) {
		this.pay_cost = pay_cost;
	}
	public int getSaving_cost() {
		return saving_cost;
	}
	public void setSaving_cost(int saving_cost) {
		this.saving_cost = saving_cost;
	}
	public String getSaving_date() {
		return saving_date;
	}
	public void setSaving_date(String saving_date) {
		this.saving_date = saving_date;
	}
	  
}
