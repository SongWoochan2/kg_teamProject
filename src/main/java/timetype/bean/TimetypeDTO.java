package timetype.bean;

public class TimetypeDTO {
	private int time_type_code;
	private int add_cost;
	private String time_type_name;
	
	
	public int getTime_type_code() {
		return time_type_code;
	}
	public void setTime_type_code(int time_type_code) {
		this.time_type_code = time_type_code;
	}
	public int getAdd_cost() {
		return add_cost;
	}
	public void setAdd_cost(int add_cost) {
		this.add_cost = add_cost;
	}
	public String getTime_type_name() {
		return time_type_name;
	}
	public void setTime_type_name(String time_type_name) {
		this.time_type_name = time_type_name;
	}
	
	
}
