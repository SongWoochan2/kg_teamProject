package theater.bean;

public class TheaterDTO {
	private int theater_code;
	private String theater_name;
	private String theater_photo_addr;
	private String theater_phone;

	
	public int getTheater_code() {
		return theater_code;
	}
	public void setTheater_code(int theater_code) {
		this.theater_code = theater_code;
	}
	public String getTheater_name() {
		return theater_name;
	}
	public void setTheater_name(String theater_name) {
		this.theater_name = theater_name;
	}
	public String getTheater_photo_addr() {
		return theater_photo_addr;
	}
	public void setTheater_photo_addr(String theater_photo_addr) {
		this.theater_photo_addr = theater_photo_addr;
	}
	public String getTheater_phone() {
		return theater_phone;
	}
	public void setTheater_phone(String theater_phone) {
		this.theater_phone = theater_phone;
	}
	
	
	
}
