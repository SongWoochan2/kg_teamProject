package noticeItem.bean;

public class NoticeItemDTO {
	private int notice_item_code;
	private String admin_id;
	private String notice_item_type;
	private String notice_item_title;
	private String notice_item_content;
	private String notice_item_date;
	
	
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}
	public int getNotice_item_code() {
		return notice_item_code;
	}
	public void setNotice_item_code(int notice_item_code) {
		this.notice_item_code = notice_item_code;
	}
	public String getNotice_item_type() {
		return notice_item_type;
	}
	public void setNotice_item_type(String notice_item_type) {
		this.notice_item_type = notice_item_type;
	}
	public String getNotice_item_title() {
		return notice_item_title;
	}
	public void setNotice_item_title(String notice_item_title) {
		this.notice_item_title = notice_item_title;
	}
	public String getNotice_item_content() {
		return notice_item_content;
	}
	public void setNotice_item_content(String notice_item_content) {
		this.notice_item_content = notice_item_content;
	}
	
	public String getNotice_item_date() {
		return notice_item_date;
	}
	public void setNotice_item_date(String notice_item_date) {
		this.notice_item_date = notice_item_date;
	}
	
	
	
	
		
}
